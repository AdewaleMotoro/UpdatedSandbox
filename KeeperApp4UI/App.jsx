import React, { useState } from "react";
import Header from "../KeeperApp4UI/Header";
import Footer from "../KeeperApp4UI/Footer";
import Note from "../KeeperApp4UI/Note";
import CreateArea from "../KeeperApp4UI/CreateArea";
import "../KeeperApp4UI/styles.css"



function App() {
  const [notes, setNotes] = useState([]);

  function addNote(newNote) {
    setNotes(prevNotes => {
      return [...prevNotes, newNote];
    });
  }

  function deleteNote(id) {
    setNotes(prevNotes => {
      return prevNotes.filter((noteItem, index) => {
        return index !== id;
      });
    });
  }

  return (
    <div>
      <Header />
      <CreateArea onAdd={addNote} />
      {notes.map((noteItem, index) => {
        return (
          <Note
            key={index}
            id={index}
            title={noteItem.title}
            content={noteItem.content}
            onDelete={deleteNote}
          />
        );
      })}
      <Footer />
    </div>
  );
}

export default App;
